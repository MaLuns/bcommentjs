export const parse = str => String(str).replace(/::(.+?)::/g, (placeholder, key) => {
    let keys = Object.keys(emojis);
    for (let index = 0; index < keys.length; index++) {
        const item = emojis[keys[index]];
        if (['image'].includes(item.type)) {
            let emoji = item.container.find(element => element.text === key)
            if (emoji) {
                return `<i class="emoji-icon"><img alt referrerpolicy="no-referrer" src="${emoji.icon}" /></i>`;
            }
        }
    }
    return placeholder
})

